#!/usr/bin/env node
// Fetches the current phrases snapshot (all keys, all languages) from the live
// phrases endpoint and writes a local, human-readable `i18n_local.js` for quick
// lookup during development. This file is gitignored and is NOT used by the app
// at runtime — the app loads its pinned version from the endpoint directly.
//
// Usage:
//   npm run i18n:local              # current published version
//   npm run i18n:local -- --v 3.2   # a specific version
//   npm run i18n:local -- --base http://localhost:8888   # against netlify dev
//
// See notes/PHRASES_VERSIONED_API_ARCHITECTURE.md for the endpoint contract.

import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, "..");

const DEFAULT_BASE = "https://easyeyes.app";
const OUTPUT_PATH = resolve(REPO_ROOT, "i18n_local.js");

function parseArgs(argv) {
  const args = { base: DEFAULT_BASE, version: null };
  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    if (arg === "--base") args.base = argv[++i];
    else if (arg === "--v" || arg === "--version") args.version = argv[++i];
    else if (arg.startsWith("--base=")) args.base = arg.slice("--base=".length);
    else if (arg.startsWith("--v=")) args.version = arg.slice("--v=".length);
  }
  return args;
}

async function fetchJson(url, attempts = 4) {
  let lastErr;
  for (let attempt = 0; attempt < attempts; attempt++) {
    if (attempt > 0) await new Promise((r) => setTimeout(r, 500 * attempt));
    try {
      const res = await fetch(url);
      if (!res.ok) {
        let detail = "";
        try {
          detail = ` — ${JSON.stringify(await res.json())}`;
        } catch {
          /* non-JSON error body */
        }
        throw new Error(`HTTP ${res.status} for ${url}${detail}`);
      }
      return await res.json();
    } catch (err) {
      lastErr = err;
    }
  }
  throw lastErr;
}

async function main() {
  const { base, version } = parseArgs(process.argv.slice(2));
  const cleanBase = base.replace(/\/+$/, "");

  // `?v=` is an immutable snapshot; the bare GET is the current published one.
  const url = version
    ? `${cleanBase}/.netlify/functions/phrases?v=${encodeURIComponent(version)}`
    : `${cleanBase}/.netlify/functions/phrases`;

  console.log(`Fetching phrases from ${url} ...`);
  const data = await fetchJson(url);

  const phrases = data?.phrases;
  if (!phrases || typeof phrases !== "object") {
    throw new Error(
      `Unexpected response shape (no "phrases" object). Got keys: ${Object.keys(
        data ?? {},
      ).join(", ")}`,
    );
  }

  const resolvedVersion = data.version ?? version ?? "unknown";
  const keyCount = Object.keys(phrases).length;
  const languages = new Set();
  for (const row of Object.values(phrases)) {
    if (row && typeof row === "object")
      for (const lang of Object.keys(row)) languages.add(lang);
  }

  // Sort keys and languages so the output is stable and diff-friendly between runs.
  const sortedPhrases = {};
  for (const key of Object.keys(phrases).sort()) {
    const row = phrases[key];
    if (row && typeof row === "object") {
      const sortedRow = {};
      for (const lang of Object.keys(row).sort()) sortedRow[lang] = row[lang];
      sortedPhrases[key] = sortedRow;
    } else {
      sortedPhrases[key] = row;
    }
  }

  const header = [
    "// AUTO-GENERATED by `npm run i18n:local` — DO NOT EDIT, DO NOT COMMIT.",
    "// Local copy of all i18n phrases and translations, for development lookup only.",
    "// The app does NOT use this file; it loads its pinned version from the endpoint.",
    `// Phrases version: ${resolvedVersion}`,
    `// Keys: ${keyCount} · Languages: ${languages.size}`,
    `// Source: ${url}`,
    `// Generated: ${new Date().toISOString()}`,
    "",
  ].join("\n");

  const body =
    `export const phrasesVersion = ${JSON.stringify(resolvedVersion)};\n\n` +
    `export const phrases = ${JSON.stringify(sortedPhrases, null, 2)};\n\n` +
    `export default phrases;\n`;

  writeFileSync(OUTPUT_PATH, header + "\n" + body, "utf-8");

  console.log(
    `Wrote ${OUTPUT_PATH}\n  version: ${resolvedVersion}\n  keys: ${keyCount}\n  languages: ${languages.size} (${[
      ...languages,
    ]
      .sort()
      .join(", ")})`,
  );
}

main().catch((err) => {
  console.error("Failed to generate i18n_local.js:");
  console.error(err?.message ?? err);
  process.exit(1);
});
