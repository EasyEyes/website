#!/usr/bin/env node
// Fetches the current glossary snapshot (all parameters and their metadata) from
// the live glossary endpoint and writes a local, human-readable `glossary_local.js`
// for quick lookup during development. This file is gitignored and is NOT used by
// the app at runtime — the app loads its pinned version from the endpoint directly.
//
// Usage:
//   npm run glossary:local              # current published version
//   npm run glossary:local -- --v 3.2   # a specific version
//   npm run glossary:local -- --base http://localhost:8888   # against netlify dev
//
// See notes/GLOSSARY_VERSIONED_API_ARCHITECTURE.md for the endpoint contract.

import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, "..");

const DEFAULT_BASE = "https://easyeyes.app";
const OUTPUT_PATH = resolve(REPO_ROOT, "glossary_local.js");

// Stable, lookup-friendly field order for each entry, regardless of how the
// endpoint happens to serialize them.
const FIELD_ORDER = [
  "name",
  "availability",
  "type",
  "default",
  "explanation",
  "example",
  "categories",
];

function parseArgs(argv) {
  const args = { base: DEFAULT_BASE, version: null };
  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    if (arg === "--base") args.base = argv[++i];
    else if (arg === "--v" || arg === "--version") args.version = argv[++i];
    else if (arg.startsWith("--base=")) args.base = arg.slice("--base=".length);
    else if (arg.startsWith("--v=")) args.version = arg.slice("--v=".length);
  }
  return args;
}

async function fetchJson(url, attempts = 4) {
  let lastErr;
  for (let attempt = 0; attempt < attempts; attempt++) {
    if (attempt > 0) await new Promise((r) => setTimeout(r, 500 * attempt));
    try {
      const res = await fetch(url);
      if (!res.ok) {
        let detail = "";
        try {
          detail = ` — ${JSON.stringify(await res.json())}`;
        } catch {
          /* non-JSON error body */
        }
        throw new Error(`HTTP ${res.status} for ${url}${detail}`);
      }
      return await res.json();
    } catch (err) {
      lastErr = err;
    }
  }
  throw lastErr;
}

function orderEntry(entry) {
  if (!entry || typeof entry !== "object") return entry;
  const ordered = {};
  for (const field of FIELD_ORDER) {
    if (field in entry) ordered[field] = entry[field];
  }
  // Preserve any unexpected extra fields rather than silently dropping them.
  for (const field of Object.keys(entry)) {
    if (!(field in ordered)) ordered[field] = entry[field];
  }
  return ordered;
}

async function main() {
  const { base, version } = parseArgs(process.argv.slice(2));
  const cleanBase = base.replace(/\/+$/, "");

  // `?v=` is an immutable snapshot; the bare GET is the current published one.
  const url = version
    ? `${cleanBase}/.netlify/functions/glossary?v=${encodeURIComponent(version)}`
    : `${cleanBase}/.netlify/functions/glossary`;

  console.log(`Fetching glossary from ${url} ...`);
  const data = await fetchJson(url);

  const glossary = data?.glossary;
  if (!glossary || typeof glossary !== "object") {
    throw new Error(
      `Unexpected response shape (no "glossary" object). Got keys: ${Object.keys(
        data ?? {},
      ).join(", ")}`,
    );
  }

  const resolvedVersion = data.version ?? version ?? "unknown";
  const paramCount = Object.keys(glossary).length;
  const superMatchingParams = Array.isArray(data.superMatchingParams)
    ? [...data.superMatchingParams].sort()
    : Object.keys(glossary)
        .filter((k) => k.includes("@"))
        .sort();

  // Sort parameters so the output is stable and diff-friendly between runs.
  const sortedGlossary = {};
  for (const key of Object.keys(glossary).sort()) {
    sortedGlossary[key] = orderEntry(glossary[key]);
  }

  const header = [
    "// AUTO-GENERATED by `npm run glossary:local` — DO NOT EDIT, DO NOT COMMIT.",
    "// Local copy of the full parameter glossary, for development lookup only.",
    "// The app does NOT use this file; it loads its pinned version from the endpoint.",
    `// Glossary version: ${resolvedVersion}`,
    `// Parameters: ${paramCount} · Super-matching params: ${superMatchingParams.length}`,
    `// Source: ${url}`,
    `// Generated: ${new Date().toISOString()}`,
    "",
  ].join("\n");

  const body =
    `export const glossaryVersion = ${JSON.stringify(resolvedVersion)};\n\n` +
    `export const superMatchingParams = ${JSON.stringify(
      superMatchingParams,
      null,
      2,
    )};\n\n` +
    `export const glossary = ${JSON.stringify(sortedGlossary, null, 2)};\n\n` +
    `export default glossary;\n`;

  writeFileSync(OUTPUT_PATH, header + "\n" + body, "utf-8");

  console.log(
    `Wrote ${OUTPUT_PATH}\n  version: ${resolvedVersion}\n  parameters: ${paramCount}\n  super-matching params: ${superMatchingParams.length}`,
  );
}

main().catch((err) => {
  console.error("Failed to generate glossary_local.js:");
  console.error(err?.message ?? err);
  process.exit(1);
});
